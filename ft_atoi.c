#include "libft.h"

int	ft_atoi(const char *str)
{
	int	n;
	int	i;
	int	neg;

	n = 0;
	i = 0;
	neg = 1;
	while (str[i] == ' ' || str[i] == '\f' || str[i] == '\n'
		|| str[i] == '\r' || str[i] == '\t' || str[i] == '\v')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{	
		n = n * 10 + (str[i] - '0');
		i++;
	}
	return (n * neg);
}
